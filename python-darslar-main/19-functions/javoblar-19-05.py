# Foydalanuvchidan x va y sonlarini olib, x^y ni konsolga chiqaruvchi funksiya yozing.
def daraja(x, y):
    """x ni y-darajaga oshiruvchi funksiya"""
    print(f"{x} ning {y}-darajasi {x**y} ga teng")


daraja(5, 2)
daraja(3, 3)
daraja(94, 4)
